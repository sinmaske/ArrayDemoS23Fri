public class Student {
    private String sId;
    private String sNAme;

    public Student(String sId, String sNAme) {
        this.sId = sId;
        this.sNAme = sNAme;
    }

    
    
    public String getsId() {
        return sId;
    }

    public void setsId(String sId) {
        this.sId = sId;
    }

    public String getsNAme() {
        return sNAme;
    }

    public void setsNAme(String sNAme) {
        this.sNAme = sNAme;
    }
    
    
}
